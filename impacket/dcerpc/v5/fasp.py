# Description:
#   MS-FASP (Firewall and Advanced Security Protocol) implementation. This protool is an RPC based protocol that revolves around
#   the management of Windows Firewall rules and policies and working/interfacing with all things Windows Firewall related remotely over
#   RPC interfaces. To use this protocol the caller would need to be a member of the Administrators (S-1-5-32-544) or Network Configuration Operators (S-1-5-32-556) groups. 
#   
#   TLDR:  the network administrator needs to be ableto remotely manage the advanced firewall and IPsec policy of a host and this is
#    the protocol that allows them to do so.
# Author: Abdul Mhanni

